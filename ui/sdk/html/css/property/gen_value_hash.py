import os
import sys
import subprocess

"""
生成property value完美hash, 用于实现O(1)将name转成id 
调用示例：
python gen_value_hash.py output_dir

输出：
value_id.template
value_id_hash.cpp
"""


SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))


def read_value_id_name_list():
  """
  从property_value.h中读取所有的属性列表
  """
  property_id_h_path = os.path.join(SCRIPT_DIR, "value_id.h")

  class StateContext:
    def __init__(self):
      self.state = 0
      self.result = []

  def state_find_enum(line, c):
    if line.startswith("enum class CSSValueId"): 
      c.state+=1
  def state_get_id_name(line, c):
    line = line.strip(" \t\n\r")
    if line.startswith("}"):
      c.state+=1
      return

    if line[:2] == "//":
      return
    
    # 格式：
    # ValueId = 99, // "ValueName"

    id = line.split(",")[0].split("=")[0].strip()
    if "//" not in line:
      return
    value = line.split("//")[-1].strip()

    if id and value:
      c.result.append((id, value))

  def state_pass(line, c):
    pass
  
  # 状态机
  state_function = [
    state_find_enum,
    state_get_id_name,
    state_pass
  ]

  c = StateContext()
  with open(property_id_h_path, "r", encoding="utf-8") as f:
    for line in f:
      state_function[c.state](line, c)

  return c.result


def gen_gperf_template(output_dir, properties):
  """
  生成gperf输入模板
  """
  # output_dir = os.path.join(SCRIPT_DIR, "gen")
  if not os.path.exists(output_dir):
    os.mkdir(output_dir)

  template_path = os.path.join(output_dir, "value_id.template")
  with open(template_path, "w", encoding="utf-8") as f:
    f.write(
"""// generated by gen_value_hash.py
%language=C++
%define class-name CSSValueIdHash
%define lookup-function-name CSSValueNameToId
%define word-array-name css_value_word_list

%{
#include "html/css/property/value_id.h"
#include <string.h>

namespace html {
%}

struct CSSValueHashEntry { 
  const char* name; 
  int id; 
};
%%
""")
    for id, name in properties:
      name = name.strip('"')
      f.write(f"{name}, (int)html::CSSValueId::{id}\n")


def run(commands: list[str], cwd_dir):
    print(" ".join(commands))
    ret = subprocess.run(commands, cwd=cwd_dir,
                         stdout=subprocess.PIPE, 
                         stderr=subprocess.PIPE)
    output = ret.stdout.decode("utf-8")
    error = ret.stderr.decode("utf-8")
    if error:
      print(error)

    return ret.returncode, output

  
def gperf(output_dir):
  # -D 不使用register关键字
  # gperf -t property_value.template > property_value_hash.cpp
  # gen_dir = os.path.join(SCRIPT_DIR, "gen")

  # TODO:  gperf.exe on windows
  gperf_path = "gperf"

  code, output = run([gperf_path, "-t", "value_id.template"], output_dir)
  hash_cpp_path = os.path.join(output_dir, "value_id_hash.cpp")
  if code != 0:
    print("run gperf failed")
    exit(1)

  # 1. 去掉register关键字，c++17已经不再支持
  output = output.replace("register ", "")

  # 2. 去掉 #line行，
  lines = output.split("\n")
  output = "\n".join([line for line in lines if not line.startswith("#line ")])

  # 2. 补全namespace后面那个括号
  suffix ="""
// 外部调用的函数。
CSSValueId CSSValueNameToIdByHash(const char* name, unsigned int len) {
  CSSValueHashEntry* entry = CSSValueIdHash::CSSValueNameToId(name, len);
  return entry ? (CSSValueId)entry->id : html::CSSValueId::Invalid;
}

} // namespace html
"""
  output += suffix

  with open(hash_cpp_path, "w", encoding="utf-8") as f:
    f.write(output)


def main(output_dir):
  properties = read_value_id_name_list()
  gen_gperf_template(output_dir, properties)
  gperf(output_dir)


if __name__ == "__main__":
  output_dir = sys.argv[1] or os.path.join(SCRIPT_DIR, "gen")
  main(output_dir)
